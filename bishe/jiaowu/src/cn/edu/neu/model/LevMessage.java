package cn.edu.neu.model;

public class LevMessage {
	private int levID;
	private String s_id;
	private String levName;
	private String levEmail;
	private String levCon;
	private String levTime;
	private int levFlag;
	public int getLevID() {
		return levID;
	}
	public void setLevID(int levID) {
		this.levID = levID;
	}
	
	public String getS_id() {
		return s_id;
	}
	public void setS_id(String s_id) {
		this.s_id = s_id;
	}
	public String getLevName() {
		return levName;
	}
	public void setLevName(String levName) {
		this.levName = levName;
	}
	public String getLevEmail() {
		return levEmail;
	}
	public void setLevEmail(String levEmail) {
		this.levEmail = levEmail;
	}
	public String getLevCon() {
		return levCon;
	}
	public void setLevCon(String levCon) {
		this.levCon = levCon;
	}
	public String getLevTime() {
		return levTime;
	}
	public void setLevTime(String levTime) {
		this.levTime = levTime;
	}
	public int getLevFlag() {
		return levFlag;
	}
	public void setLevFlag(int levFlag) {
		this.levFlag = levFlag;
	}
	
	
}
